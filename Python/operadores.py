# > Operadores aritméticos

"""
- Soma: +
- Subtração: - 
- Multiplicação: *
- Divisão: /
- Divisão inteira: // -> descarta a parte decimal
- Resto da divisão: %
- Potência: ** 
"""
numero1 =  10
numero2 =  20

print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 * numero2)
print(numero1 / numero2)
print(numero1 // numero2)
print(numero1 % numero2)
print(numero1 ** numero2)


